# transition გამოიყენება იმისთვის, რომ ელემენტის სტილები გლუვად (ანიმაციით) შეიცვალოს, მაგალითად ფონის ფერის ან ზომის ცვლილება.

# ქვე-კუთვნილებები:
# transition-property – რომელ თვისებაზე მოხდეს გადასვლა (მაგ: background-color, width, ან all).

# transition-duration – რამდენი ხანი გაგრძელდეს ტრანზიცია (მაგ: 0.5s ან 300ms).

# transition-timing-function – ტრანზიციის სიჩქარის მრუდი:

# ease – ნელა იწყება და ნელა მთავრდება (ნაგულისხმევია)

# linear – თანაბარი სიჩქარე

# ease-in – ნელა იწყება

# ease-out – ნელა მთავრდება

# ease-in-out – ნელა იწყება და ნელა მთავრდება

# transition-delay (არ მოვთხოვეთ, მაგრამ არსებობს) – რამდენ ხანში დაიწყოს გადასვლა

